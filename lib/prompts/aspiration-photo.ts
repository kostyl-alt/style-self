// 憧れ写真分析の user メッセージ用 context（列絞り SELECT 由来の日本語サマリのみ）。
// 英語スラッグ・worldview_tags・内部 ID は構造的に含まれない（三重防御1）。
export interface AspirationPhotoContext {
  worldviewName:     string | null;
  worldviewKeywords: string[];
  coreIdentity:      string | null;
  bodyProfile?: {
    height:         number | null;
    bodyType:       string | null;
    skeletonType:   string | null;
    proportionNote: string | null;
  };
  avoidImpressions?: string[];
  avoidItems?:       string[];
}

function truncate(s: string, n: number): string {
  return s.length <= n ? s : s.slice(0, n) + "…";
}

// 画像と一緒に Claude へ渡す user テキストを組み立てる。
// note は textarea の任意補足（「この〇〇が好き」等）。空なら省く。
export function buildAspirationPhotoUserMessage(
  ctx: AspirationPhotoContext,
  note?: string,
): string {
  const lines: string[] = [];
  const hasWorldview = Boolean(ctx.worldviewName) || ctx.worldviewKeywords.length > 0 || Boolean(ctx.coreIdentity);
  const b = ctx.bodyProfile;
  const hasBody = Boolean(b && (b.height != null || b.bodyType || b.skeletonType || b.proportionNote));

  lines.push("【このユーザーの文脈（本人のみ・日本語サマリ・変換先の補助に使う）】");

  // 世界観: 診断・履歴に根拠があるときだけ提示。無ければ「未診断」と明示し、断定させない。
  if (hasWorldview) {
    lines.push("◯ 診断・履歴に根拠あり（この方向に“控えめに”寄せてよい。新しい世界観名を作らない）:");
    if (ctx.worldviewName)            lines.push(`・世界観名: ${ctx.worldviewName}`);
    if (ctx.worldviewKeywords.length) lines.push(`・日本語キーワード: ${ctx.worldviewKeywords.slice(0, 6).join("、")}`);
    if (ctx.coreIdentity)             lines.push(`・核となる方向性: ${truncate(ctx.coreIdentity, 80)}`);
  } else {
    lines.push("◯ 世界観: 未診断（根拠なし）。→ 世界観を断定・新造せず、写真の構造の再現に留める。推測は推測と分かる言い方で。");
  }

  // 体型: 「成立条件」の材料（欠点として扱わない）。診断有無に関わらず登録があれば渡す。
  if (hasBody && b) {
    lines.push("◯ 体型（“成立条件”の材料。欠点として指摘せず、こうすると良くなる、の形で使う）:");
    if (b.height != null)  lines.push(`・身長: ${b.height}cm`);
    if (b.bodyType)        lines.push(`・体型: ${b.bodyType}`);
    if (b.skeletonType)    lines.push(`・骨格: ${b.skeletonType}`);
    if (b.proportionNote)  lines.push(`・体型の補足: ${truncate(b.proportionNote, 60)}`);
  }

  if (ctx.avoidImpressions && ctx.avoidImpressions.length) lines.push(`◯ 避けたい印象: ${ctx.avoidImpressions.slice(0, 4).join("、")}`);
  if (ctx.avoidItems && ctx.avoidItems.length)             lines.push(`◯ 避けたい服: ${ctx.avoidItems.slice(0, 6).join("、")}`);

  if (!hasWorldview && !hasBody) {
    lines.push("→ 文脈がほぼ無いので、写真の構造の話に留め、他に2〜3枚送ると好みの方向が掴める旨を押し付けず添える。");
  }

  if (note && note.trim()) {
    lines.push("");
    lines.push(`【ユーザーからの補足】${truncate(note.trim(), 300)}`);
  }

  lines.push("");
  lines.push("この憧れ写真を、まず「核」を1〜2文で言い切ってから、3層ルール・体型=成立条件・世界観は断定しない・具体的な言葉、を守って自然に読み解いてください。参照スタイルは8軸（ジャンル×年代×カルチャー×色×シルエット×素材×ムード）で分解し、複数参照の混合も見抜き、末尾に「参照スタイルの分解」整理ブロック（検索ワード含む）を必ず付けてください。素材感は「写真上では」と前置きして光沢・質感から方向を推定（断定しない）。丸パクリでなく、同じ構造を別アイテムで再現する置き換えで、あなたらしく近づける形にしてください。");

  return lines.join("\n");
}
