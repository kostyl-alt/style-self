// プロンプト注入用の語彙ブロック。genre/culture は「名前 = 特徴」を1行ずつ、閉じた小集合は名前列挙。
export function buildStyleTaxonomyBlock(): string {
  const out: string[] = [
    "【参照スタイルの語彙（検索候補の足がかり・断定に使わない・見えた事実属性に近いものだけ理由付きで挙げる・ここに無い適切な語も可）】",
  ];
  for (const axis of STYLE_AXES) {
    const hasId = axis.tags.some((tag) => tag.identifier);
    if (hasId) {
      out.push(`■ ${axis.axisLabel}（名前 = 特徴）`);
      for (const tag of axis.tags) out.push(`- ${tag.name} = ${tag.identifier}`);
    } else {
      out.push(`■ ${axis.axisLabel}: ${axis.tags.map((tag) => tag.name).join(" / ")}`);
    }
  }
  return out.join("\n");
}
