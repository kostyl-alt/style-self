// プロンプト注入用の代表語彙ブロック（コンパクト・トークン節約）。
export function buildStyleTaxonomyBlock(): string {
  const lines = STYLE_AXES.map((a) => `- ${a.axisLabel}: ${a.tags.join(" / ")}`);
  return [
    "【参照スタイルの語彙（代表タグ・この語彙を“足がかり”に、知っている範囲で具体化する。ここに無い適切な語を使ってよい）】",
    ...lines,
  ].join("\n");
}
