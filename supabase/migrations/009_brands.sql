-- Sprint 19: brands table for brand recommendation feature

create table public.brands (
  id             uuid primary key default gen_random_uuid(),
  name           text not null,
  name_ja        text,
  country        text not null,
  city           text,
  description    text not null,
  worldview_tags text[] not null default '{}',
  taste_tags     text[] not null default '{}',
  era_tags       text[] not null default '{}',
  scene_tags     text[] not null default '{}',
  price_range    text not null check (price_range in ('budget','mid','high','luxury')),
  maniac_level   integer not null check (maniac_level between 1 and 5),
  official_url   text,
  instagram_url  text,
  is_active      boolean not null default true,
  created_at     timestamptz not null default now()
);

alter table public.brands enable row level security;

create policy "brands are publicly readable"
  on public.brands for select using (true);

